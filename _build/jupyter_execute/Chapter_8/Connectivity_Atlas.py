# The [Allen Mouse Brain Connectivity Atlas](http://connectivity.brain-map.org/) contains a brain-wide map of connections in the mouse brain, arguably the most comprehensive wiring diagram of a mammalian brain yet. This dataset uses different types of virus to trace neural circuits in specific cell-types. Usefully, the Allen Institute has also compiled an SDK so that you can interact directly with the data. See [their website](http://help.brain-map.org/display/mouseconnectivity/Allen+Mouse+Brain+Connectivity+Atlas) for more information about the data and how it was collected.
